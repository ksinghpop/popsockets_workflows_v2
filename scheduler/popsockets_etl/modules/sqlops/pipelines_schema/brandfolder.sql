-- Organisation
create or replace TABLE PROD.BRANDFOLDER.ORGANISATION (
	ID VARCHAR(16777216) NOT NULL,
	TYPE VARCHAR(16777216),
	ATTRIBUTES_NAME VARCHAR(16777216),
	ATTRIBUTES_SLUG VARCHAR(16777216),
	ATTRIBUTES_TAGLINE VARCHAR(16777216),
	ATTRIBUTES_CREATED_AT TIMESTAMP_TZ(9),
	ATTRIBUTES_UPDATED_AT TIMESTAMP_TZ(9),
	primary key (ID)
);

create or replace TABLE PROD.BRANDFOLDER.BRANDFOLDER (
	ID VARCHAR(16777216) NOT NULL PRIMARY KEY,
	TYPE VARCHAR(16777216),
	ATTRIBUTES_NAME VARCHAR(16777216),
	ATTRIBUTES_TAGLINE VARCHAR(16777216),
	ATTRIBUTES_PRIVACY VARCHAR(16777216),
	ATTRIBUTES_SLUG VARCHAR(16777216),
	ATTRIBUTES_CREATED_AT TIMESTAMP_TZ(9),
	ATTRIBUTES_UPDATED_AT TIMESTAMP_TZ(9),
	ORGANISATION_ID VARCHAR(16777216) FOREIGN KEY REFERENCES ORGANISATION(id)
);

